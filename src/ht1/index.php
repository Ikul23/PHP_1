<?php

// Нестрогое сравнение
$a = 5;
$b = '05';
var_dump($a == $b);          // true (нестрогое сравнение, '05' -> 5)

// Преобразование строки с лидирующим нулем
var_dump((int)'012345');     // 12345 (лидирующий 0 отбрасывается)

// Сравнение float и int
var_dump((float)123.0 === (int)123.0); // false (разные типы)

// Преобразование строки в число
var_dump(0 == 'hello, world'); // true (строка преобразуется в 0)

// Дополнительные проверки
echo "\nДополнительные проверки:\n";
var_dump('7' == 7);          // true (нестрогое сравнение)
var_dump('7' === 7);         // false (строгое сравнение)